        // Logout Confirmation
        function confirmLogout() {
            Swal.fire({
                title: 'Konfirmasi Logout',
                text: 'Apakah Anda yakin ingin keluar?',
                icon: 'question',
                showCancelButton: true,
                confirmButtonColor: '#1e3c72',
                cancelButtonColor: '#6c757d',
                confirmButtonText: 'Ya, Keluar',
                cancelButtonText: 'Batal'
            }).then((result) => {
                if (result.isConfirmed) {
                    document.getElementById('logout-form').submit();
                }
            });
        }

        // Set active navigation based on current URL
        document.addEventListener('DOMContentLoaded', function() {
            const currentUrl = window.location.pathname;
            
            // Set active nav item
            document.querySelectorAll('.nav-item').forEach(item => {
                if (item.getAttribute('href') === currentUrl) {
                    item.classList.add('active');
                }
            });
            
            // Set active sidebar item
            document.querySelectorAll('.sidebar-item').forEach(item => {
                if (item.getAttribute('href') === currentUrl) {
                    item.classList.add('active');
                }
            });
        });

        // Handle image loading errors for avatars
        document.addEventListener('DOMContentLoaded', function() {
            document.querySelectorAll('.avatar-image').forEach(img => {
                img.addEventListener('error', function() {
                    this.style.display = 'none';
                    const initials = this.nextElementSibling;
                    if (initials && initials.classList.contains('avatar-initials')) {
                        initials.style.display = 'flex';
                    }
                });
            });
        });
    </script>

